let scale = 10;

let palette = [];

const gridWidth = spaceInvader[0].length * scale;
const gridHeight = spaceInvader.length * scale;

const colors = [makeTheWorldFeelSomethingDeep, moneyBleeds, bkBotanicalGardens, thirdAve, trafficOfficer];

function getColorFromValue(value) {
  return palette[value];
}

function setup() {
  createCanvas(gridWidth, gridHeight);

  tileSize = scale;
  palette = colors[Math.floor(Math.random() * colors.length)];
}

function draw() {
  background(255);

  for (let x = 0; x < gridWidth; x += tileSize) {
    for (let y = 0; y < gridHeight; y += tileSize) {
      rect(x, y, tileSize, tileSize);
    }
  }

  let invaderSizeX = gridWidth / scale;
  let invaderSizeY = gridHeight / scale;

  for (let i = 0; i < invaderSizeX; i++) {
    for (let j = 0; j < invaderSizeY; j++) {
      let x = i * tileSize;
      let y = j * tileSize;

      fill(getColorFromValue(spaceInvader[j][i]));

      rect(x, y, tileSize, tileSize);
    }
  }

  noLoop();
}
